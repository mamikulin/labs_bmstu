#include "mishaslib.hpp"

namespace mishaslib {

    double mishaslib::Add(double a, double b){
        return a + b;
    }
    double mishaslib::Subtract(double a, double b){
        return a - b;
    }

    double mishaslib::Dvi(double a, double b){
        return a/b;
    }

    double mishaslib::Mult(double a, double b){
        return a*b;
    }
}


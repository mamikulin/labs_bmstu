#include "mymath.h"

namespace math{

    double Mymath::Add(double a, double b){
        return a + b;
    }
    double Mymath::Subtract(double a, double b){
        return a - b;
    }

    double Mymath::Dvi(double a, double b){
        return a/b;
    }

    double Mymath::Mult(double a, double b){
        return a*b;
    }
}

